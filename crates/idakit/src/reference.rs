//! Cross-references: [`Reference`] and its [`ReferenceKind`] classification. The IDA
//! reference-type byte is in two overlapping spaces (code/data) split here into
//! [`CodeReference`]/[`DataReference`], each a closed mirror of `cref_t`/`dref_t` that
//! rejects a byte outside the set rather than folding it into a catch-all.

use std::ffi::c_void;
use std::marker::PhantomData;

use idakit_sys as sys;
use num_enum::{IntoPrimitive, TryFromPrimitive};
use strum::VariantArray;

use crate::Idb;
use crate::address::Address;

impl Idb {
    /// Lazily iterate every cross-reference targeting `address` -- its callers and the data
    /// that points at it (ordinary sequential flow excluded).
    #[inline]
    #[must_use]
    pub fn references_to(&self, address: Address) -> References<'_> {
        References::new(self.xref_open(address, true))
    }

    /// Lazily iterate every cross-reference originating at `address` -- what the code there
    /// calls, jumps to, or reads (ordinary sequential flow excluded).
    #[inline]
    #[must_use]
    pub fn references_from(&self, address: Address) -> References<'_> {
        References::new(self.xref_open(address, false))
    }
}

/// A cross-reference edge, carrying both endpoints. For [`references_to`](Idb::references_to) the
/// `to` end is the queried address; for [`references_from`](Idb::references_from) the `from` end is.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub struct Reference {
    /// The referencing (source) address.
    pub from: Address,
    /// The referenced (target) address.
    pub to: Address,
    /// How the reference is classified -- code vs data, and its specific type.
    pub kind: ReferenceKind,
    /// Who created the reference -- IDA's analysis or the user.
    pub origin: Origin,
}

impl Reference {
    /// Build from the facade's `(from, to, type, iscode, user)` tuple. `None` -- and so skipped by
    /// the iterator -- if either endpoint is `BADADDR`, or if `ty` is outside the modelled
    /// `cref_t`/`dref_t` set (version drift: the set is complete for 9.3, so this does not occur
    /// on real data). `ty` is IDA's already-masked base type; `user` is `xrefblk_t::user`.
    #[inline]
    #[must_use]
    pub(crate) fn from_raw(from: u64, to: u64, ty: u8, iscode: u8, user: u8) -> Option<Self> {
        let from = Address::try_new(from)?;
        let to = Address::try_new(to)?;
        let kind = if iscode != 0 {
            ReferenceKind::Code(CodeReference::try_from(ty).ok()?)
        } else {
            ReferenceKind::Data(DataReference::try_from(ty).ok()?)
        };
        let origin = if user != 0 {
            Origin::User
        } else {
            Origin::Analysis
        };
        Some(Self {
            from,
            to,
            kind,
            origin,
        })
    }

    /// Whether this is a code reference rather than a data one.
    #[inline]
    #[must_use]
    pub const fn is_code(&self) -> bool {
        matches!(self.kind, ReferenceKind::Code(_))
    }

    /// Whether the user defined this reference (rather than IDA's analysis).
    #[inline]
    #[must_use]
    pub const fn is_user(&self) -> bool {
        matches!(self.origin, Origin::User)
    }
}

/// Who created a cross-reference. Mirrors `xrefblk_t::user` (xref.hpp, IDA 9.3).
///
/// IDA regenerates a function's xrefs on every reanalysis, deleting the ones it made and
/// recreating them from the code -- except those a user marked, which carry the `XREF_USER`
/// bit and are preserved. So the distinction is also one of durability: a
/// [`User`](Origin::User) edge is an explicit annotation, an [`Analysis`](Origin::Analysis) edge
/// is a derived fact that moves with the code.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub enum Origin {
    /// Generated by IDA's auto-analysis (the processor module); recreated on reanalysis.
    Analysis,
    /// Defined by the user; marked `XREF_USER`, so IDA preserves it across reanalysis.
    User,
}

/// Lazy iterator over cross-references; closes the cursor on drop. Borrows `&Idb`, so it
/// can't outlive the database or coexist with a write.
pub struct References<'db> {
    cursor: *mut c_void,
    _db: PhantomData<&'db Idb>,
}

impl References<'_> {
    #[inline]
    pub(crate) fn new(cursor: *mut c_void) -> Self {
        Self {
            cursor,
            _db: PhantomData,
        }
    }
}

impl Iterator for References<'_> {
    type Item = Reference;

    fn next(&mut self) -> Option<Reference> {
        loop {
            let (mut from, mut to, mut ty, mut iscode, mut user) = (0u64, 0u64, 0u8, 0u8, 0u8);
            // SAFETY: `cursor` came from `idakit_xref_open` and is live until our `Drop`;
            // the out-pointers are valid for this call.
            let ok = unsafe {
                sys::idakit_xref_next(
                    self.cursor,
                    &mut from,
                    &mut to,
                    &mut ty,
                    &mut iscode,
                    &mut user,
                )
            };
            if ok == 0 {
                return None;
            }
            // A `BADADDR` endpoint is not a usable edge; skip it and keep stepping.
            if let Some(reference) = Reference::from_raw(from, to, ty, iscode, user) {
                return Some(reference);
            }
        }
    }
}

impl Drop for References<'_> {
    fn drop(&mut self) {
        // SAFETY: `cursor` came from `idakit_xref_open` and is closed exactly once here.
        unsafe { sys::idakit_xref_close(self.cursor) };
    }
}

/// A reference classified into the code or data type space.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub enum ReferenceKind {
    /// A code reference (call, jump, or ordinary flow).
    Code(CodeReference),
    /// A data reference (read, write, offset, ...).
    Data(DataReference),
}

/// Code cross-reference type -- a closed mirror of IDA's `cref_t` (xref.hpp, IDA 9.3). A byte
/// outside this set does not decode -- its `TryFrom<u8>` conversion fails.
#[derive(
    Clone, Copy, Debug, PartialEq, Eq, Hash, TryFromPrimitive, IntoPrimitive, VariantArray,
)]
#[repr(u8)]
pub enum CodeReference {
    /// `fl_U`: IDA's own "unknown" flow type, kept for compatibility with old databases -- a
    /// real `cref_t` value, not a catch-all for unrecognized bytes.
    Unknown = 0,
    /// A far call.
    CallFar = 16,
    /// A near call.
    CallNear = 17,
    /// A far jump.
    JumpFar = 18,
    /// A near jump.
    JumpNear = 19,
    /// `fl_USobsolete`: an obsolete user-specified code xref; modern IDA does not emit it, kept
    /// for a complete mirror of the SDK set.
    UserObsolete = 20,
    /// Ordinary sequential flow into the next instruction.
    Flow = 21,
}

/// Data cross-reference type -- a closed mirror of IDA's `dref_t` (xref.hpp, IDA 9.3). A byte
/// outside this set does not decode -- its `TryFrom<u8>` conversion fails.
#[derive(
    Clone, Copy, Debug, PartialEq, Eq, Hash, TryFromPrimitive, IntoPrimitive, VariantArray,
)]
#[repr(u8)]
pub enum DataReference {
    /// `dr_U`: IDA's own "unknown" data type, kept for compatibility with old databases -- a
    /// real `dref_t` value, not a catch-all for unrecognized bytes.
    Unknown = 0,
    /// An offset (address-of) reference.
    Offset = 1,
    /// A write access.
    Write = 2,
    /// A read access.
    Read = 3,
    /// A textual reference (forced operands only).
    Text = 4,
    /// An informational reference.
    Informational = 5,
    /// `dr_S`: a reference to an enum member / symbolic constant.
    Symbolic = 6,
}

#[cfg(test)]
mod tests {
    use assert2::assert;
    use rstest::rstest;

    use super::*;

    /// The `(type, iscode)` byte pair classifies into the right space and variant, `iscode`
    /// selecting the space so the same byte reads differently as code vs data.
    #[rstest]
    #[case::call_near(17, 1, ReferenceKind::Code(CodeReference::CallNear))]
    #[case::jump_near(19, 1, ReferenceKind::Code(CodeReference::JumpNear))]
    #[case::flow(21, 1, ReferenceKind::Code(CodeReference::Flow))]
    #[case::data_write(2, 0, ReferenceKind::Data(DataReference::Write))]
    #[case::data_read(3, 0, ReferenceKind::Data(DataReference::Read))]
    #[case::symbolic(6, 0, ReferenceKind::Data(DataReference::Symbolic))]
    fn classifies_by_type_byte(#[case] ty: u8, #[case] iscode: u8, #[case] expect: ReferenceKind) {
        let x = Reference::from_raw(0x1000, 0x2000, ty, iscode, 0).expect("valid edge");
        assert!(x.kind == expect);
        assert!(x.is_code() == matches!(expect, ReferenceKind::Code(_)));
        assert!(x.from.get() == 0x1000);
        assert!(x.to.get() == 0x2000);
    }

    /// The `user` byte maps to [`Origin`]: nonzero is a user-defined edge, zero is one IDA's
    /// analysis generated.
    #[rstest]
    #[case::analysis(0, Origin::Analysis, false)]
    #[case::user(1, Origin::User, true)]
    fn origin_reflects_user_flag(#[case] user: u8, #[case] origin: Origin, #[case] is_user: bool) {
        let x = Reference::from_raw(0x1000, 0x2000, 3, 0, user).expect("valid edge");
        assert!(x.origin == origin);
        assert!(x.is_user() == is_user);
    }

    /// A type byte outside the `cref_t`/`dref_t` set does not decode, so the iterator skips it
    /// rather than folding it into a catch-all.
    #[rstest]
    #[case::unmapped_code(99, 1)]
    #[case::unmapped_data(99, 0)]
    fn unmapped_type_is_skipped(#[case] ty: u8, #[case] iscode: u8) {
        assert!(Reference::from_raw(0x1000, 0x2000, ty, iscode, 0).is_none());
    }

    /// Every modelled variant round-trips through its raw `cref_t`/`dref_t` discriminant, so a
    /// variant whose discriminant drifts from the SDK value fails here.
    #[test]
    fn every_variant_round_trips() {
        for &c in CodeReference::VARIANTS {
            assert!(CodeReference::try_from(u8::from(c)).ok() == Some(c));
        }
        for &d in DataReference::VARIANTS {
            assert!(DataReference::try_from(u8::from(d)).ok() == Some(d));
        }
    }

    /// A `BADADDR` at either endpoint is not a usable edge.
    #[test]
    fn badaddr_endpoint_is_rejected() {
        let bad = crate::address::BADADDR;
        assert!(Reference::from_raw(bad, 0x2000, 3, 0, 0).is_none());
        assert!(Reference::from_raw(0x1000, bad, 3, 0, 0).is_none());
    }
}
